#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t i;
	size_t j;

	i = 0;
	if (!*needle)
		return ((char *)haystack);
	if (len == 0)
		return (0);
	while (haystack[i] && i < len)
	{
		j = 0;
		while (haystack[i + j] && needle[j] && haystack[i + j] == needle[j]
				&& i + j < len)
			j++;
		if (!needle[j])
			return ((char *)(haystack + i));
		if (j + i >= len)
			return (0);
		i++;
	}
	return (0);
}
